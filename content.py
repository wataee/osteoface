def build_funnel_stats_text(analytics: dict) -> str:
    """Формирует читаемый отчёт по аналитике воронки для администратора."""
    stage_labels = {
        "start":                "🚪 Зашли в бот",
        "silent_after_start":   "😶 Зашли, но ничего не нажали (1ч+)",
        "problem_chosen":       "✅ Выбрали проблему",
        "video_shown":          "▶️ Посмотрели видео",
        "prelude_shown":        "📝 Дошли до оффера 3000₽",
        "razbor_pay_clicked":   "💳 Нажали «Оплатить разбор»",
        "razbor_paid":          "💎 Оплатили разбор (3000₽)",
        "protocol_pay_clicked": "💳 Нажали «Оплатить протокол»",
        "protocol_paid":        "🎯 Оплатили протокол (7000₽)",
        "course_clicked":       "💳 Нажали «Купить курс»",
        "course_paid":          "🏆 Оплатили курс (49000₽)",
        "webinar_registered":   "🎓 Записались на вебинар",
        "webinar_attended":     "✅ Были на вебинаре",
    }
    lines = ["📊 <b>Аналитика воронки (где теряются люди)</b>\n"]
    for key, label in stage_labels.items():
        cnt = analytics.get(key, 0)
        lines.append(f"{label}: <b>{cnt}</b>")
    return "\n".join(lines)


